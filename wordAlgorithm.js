function swapElements(arr, indexA, indexB) {
    // שים לב לסוגריים המרובעים משני הצדדים - זה הקסם
    [arr[indexA], arr[indexB]] = [arr[indexB], arr[indexA]];
}

function getXthPrimeAfterY(x, y) {
    let count = 0;
    let currentNum = y + 1;

    // פונקציית עזר לבדיקת ראשוניות
    const isPrime = (num) => {
        if (num < 2) return false;
        if (num === 2) return true;
        if (num % 2 === 0) return false; // אופטימיזציה: דילוג על זוגיים
        const limit = Math.sqrt(num);
        for (let i = 3; i <= limit; i += 2) {
            if (num % i === 0) return false;
        }
        return true;
    };

    while (true) {
        if (isPrime(currentNum)) {
            count++;
            if (count === x) {
                return currentNum; // מצאנו את המספר ה-X
            }
        }
        currentNum++;
    }
}

